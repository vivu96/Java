public class rohan {
    public static void main(String[] args) {
        int n=10;
        for (int i=0; i<n; i++) {
            for (int j=0; j<n; j++) {
                 if (i==n/2 && j==n/2) {
                    System.out.print("0");
                 }
                 else {
                    System.out.print("*");
                 }
    }
    System.out.println();
}
    }
}